function monthPrinter(number){
    
    if (number === 1){
        console.log('January')
    } else if (number === 2){
        console.log('February')
    } else if (number === 3){
        console.log('March')
    } else if (number === 4){
        console.log('April')
    } else if (number === 5){
        console.log('May')
    } else if (number === 6){
        console.log('June')
    } else if (number === 7){
        console.log('July')
    } else if (number === 8){
        console.log('August')
    } else if (number === 9){
        console.log('September')
    } else if (number === 10){
        console.log('October')
    } else if (number === 11){
        console.log('November')
    } else if (number === 12){
        console.log('December')
    } else {
        console.log('Error!')
    }
    
}
monthPrinter(3)
monthPrinter(13)
